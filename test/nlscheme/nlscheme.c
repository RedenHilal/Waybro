void ieee_eid_scheme(int indent, int size, u8 * start,
					 const char * names[], int max_types)
{
	int idx = 0;
	u8 ie_size, ie_id, adv;
	while (idx + 2 <= size){
		ie_id = *start;
		ie_size = *(start + 1);

		if (idx + 2 + ie_size > size){
			break;
		}

		const char * name = NULL;
		if (ie_id < max_types && names[ie_id] != NULL){
			name = names[ie_id];
		} else {
			name = "Unknow eid type";
		}

		printf("%s size = %u\n", name, ie_size);

		adv = ie_size + 2;
		idx += adv;
		start += adv;
	}
}

void nl_msg_scheme(int indent, int size, u8 * start, 
				   const char * names[], int max_types)
{
	int idx = 0;
	u16 * as, * ai, adv;
	while (idx + 4 <= size){
		as = (u16 *) start;
		ai = (u16 *) (start + 2);

		if (*as < 4)
            break;  
        if (idx + *as > size)
            break;  

		adv = (*as + 3) & ~3;
		idx += adv;
		start += adv;

		const char * desc;

		if (*ai > max_types){
			desc = "Unknown (Vendor specific)";
		} else {
			desc = names[*ai];
		}

		printf("%s size = %u\n", desc, * as);
	}
}



